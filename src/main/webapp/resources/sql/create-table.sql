DROP TABLE IF EXISTS `sms`;
CREATE TABLE `sms` (
  `ID` bigint(20) NOT NULL,
  `CREATEDAT` datetime DEFAULT NULL,
  `PASSWORD` varchar(255) DEFAULT NULL,
  `RECEIVEDMESSAGE` longtext,
  `RETIRECOMMENTS` varchar(255) DEFAULT NULL,
  `RETIRED` tinyint(1) DEFAULT '0',
  `RETIREDAT` datetime DEFAULT NULL,
  `SENDINGMESSAGE` longtext,
  `SENDINGURL` varchar(255) DEFAULT NULL,
  `USERID` varchar(255) DEFAULT NULL,
  `BILL_ID` bigint(20) DEFAULT NULL,
  `CREATER_ID` bigint(20) DEFAULT NULL,
  `PATIENTINVESTIGATION_ID` bigint(20) DEFAULT NULL,
  `PATIENTREPORT_ID` bigint(20) DEFAULT NULL,
  `RETIRER_ID` bigint(20) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `FK_SMS_PATIENTINVESTIGATION_ID` (`PATIENTINVESTIGATION_ID`),
  KEY `FK_SMS_RETIRER_ID` (`RETIRER_ID`),
  KEY `FK_SMS_BILL_ID` (`BILL_ID`),
  KEY `FK_SMS_CREATER_ID` (`CREATER_ID`),
  CONSTRAINT `FK_SMS_BILL_ID` FOREIGN KEY (`BILL_ID`) REFERENCES `bill` (`ID`),
  CONSTRAINT `FK_SMS_CREATER_ID` FOREIGN KEY (`CREATER_ID`) REFERENCES `webuser` (`ID`),
  CONSTRAINT `FK_SMS_PATIENTINVESTIGATION_ID` FOREIGN KEY (`PATIENTINVESTIGATION_ID`) REFERENCES `patientinvestigation` (`ID`),
  CONSTRAINT `FK_SMS_RETIRER_ID` FOREIGN KEY (`RETIRER_ID`) REFERENCES `webuser` (`ID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;
